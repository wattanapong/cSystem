$m = array("มกราคม","กุมภาพันธ์","มีนาคม","เมษายน","พฤษภาคม","มิถุนายน","กรกฏาคม","สิงหาคม","กันยายน","ตุลาคม","พฤศจิกายน","ธันวาคม");
function Date2Sql( $date ){
	global $m;
	$d = explode(" ",trim($date));
	$d = ($d[2]-543)."-".(array_search($d[1],$m)+1)."-".$d[0];
	return $d;
}

echo Date2Sql("2 มกราคม 2557");
